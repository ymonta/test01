　インフォメーションでもお知らせしましたが、先週の土曜日、現在大ヒット上映中の映画『MW－ムウ－』の初日舞台あいさつが行われました。有楽町で
行われた舞台あいさつには、いしらさんのほか、山田孝之さん、山下リオさん、石橋凌さん、そして岩本仁志監督が出席（玉木宏さんからはＶＴＲメッセ
ージがありました）。観客のみなさんを前に、作品に対する意気込みや撮影時のエピソードなどを披露しました。 <br />

