幸せな映画だなと思いました。私が演じた牧野京子という役は、いままでにないタイプの役で、全速力で走るシーンをはじめ、動きがあってとても楽しかっ
たです。ぜひご覧になってください」と話してくれました。シャオメイも拝見しましたが、その迫力に圧倒され、「これは絶対に映画館で観る映画だ！」
と思いました。いしらさんの熱演も必見ですよ！ ぜひみなさんも映画館の大スクリーンで楽しんでください。以上、シャオメイがお伝えしました。

