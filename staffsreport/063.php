したが、実際に出来上がった作品を観て、いままでに観たことのない映画だと思いました。ものすごいスピードとものすごいカメラワークなので、覚悟し
てご覧になってください。社交的な玉木さんと人見知りな山田さんのコントラストもとてもいい感じだと思います。ぜひ楽しんでください」とごあいさつ。
みなさんがそれぞれあいさつした後には、「祝☆大ヒット」と書かれた「Ｍ」と「Ｗ」というパネルを持ってフォトセッションも行われました。<br />

<?php p('p5.jpg', 'r', 'パネルを持って写真撮影'); ?>

