　この日、有楽町で行なわれた舞台あいさつには、いしらさんのほか、塙幸成監督、三浦友和さんが参加。いしらさんは、「本当に美しい優しい素敵な映画だと思います。たぶん、みなさんの年齢や人生経験によって感想はいろいろだと思うのですが、このお話が実際にあったことであるということだけ頭の隅っこにおいて、後は何も考えずに見ていただければと思います。どうぞいい時間を過ごしてください」とごあいさつ。夫役の三浦さんとの共演について聞かれると「私にとって三浦友和さんという存在は光輝くスター。その三浦さんを"おっさん"と呼ぶのは、原作がそうなだからなんですけど。最初はいたたまれない気持ちでした。私がどんな芝居をしてもちゃんと受け止めて下さいますし、本当にこんな素敵な方が旦那さんだったらいいなぁといつも思っていました！」とコメント。<br />

