『眉山』はご存知、今年の５月に公開されたヒット映画。徳島を舞台に描かれた母と娘をめぐる感動の物語です。さだ作品としては初の舞台化
となる今作品では、映画に続いて宮本信子さんが母親・龍子を演じ、そして娘役・咲子を我らがいしらさんが演じます。“眉山”とは、この作品
の舞台でもある徳島県にそびえ立つ標高290メートルの美しく穏やかな山。どの方向から見ても、“眉”の格好をしていることからこの名前がつ
