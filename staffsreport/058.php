 会見に出席したいしらさんは、「私が演じた本庄久美子は被害者側の人間。とっても辛い思いをした母親の役だったので、実際にその辛さをどう表現したらよいかというのを非常に悩みましたし、実際にそういうことがあったらどんな気持ちなんだろうと思うと、本当に胸が詰まる撮影期間でした。でも、そのシーンを最初から最後まで何回も撮るというドキュメンタリータッチの手法だったので、そこに身を投じていると自然とその気持ちになるような不思議な撮影現場でした。出来上がった作品を観たときに、善悪だけではなく、どの人間もある一点の光に向かって生きていこうとするんだなという、その光のようなものを君塚監督が描いていて、とても素晴らしい映画だと思いました」とコメント。<br>

<?php p('p6.jpg', 'l', '写真撮影タイムのいしらさん'); ?>
 　
 いしらさんのコメントにもあったように、撮影は同じシーンを何度も手持ちカメラで撮るという手法で行われたそうで、臨場感のあるドキュメンタリータッチの作品となっています。テーマはとてもシリアスですが、会見ではみなさんからユニークな発言も飛び出し、その息の合ったやりとりに、チームワークの良さを感じる賑やかなひとときとなりました。<br>

<?php p('p7.jpg', 'c', '出席者のみなさんと揃って記念撮影。本当に豪華メンバーですね'); ?>
 　
