<?php p('p2.jpg', 'l', '席に座ったばかりのいしらさん。少し緊張気味！？'); ?>
 　
 都内ホテルで行われた会見には、いしらさんのほか、佐藤浩市さん、志田未来さん、松田龍平さん、佐々木蔵之介さん、佐野史郎さん、柳葉敏郎さん、そして君塚良一監督と、本作の製作総指揮・亀山千広氏が出席。作品に対する意気込みやみどころなどを披露しました。<br>

<?php p('p3.jpg', 'r', ''); ?>
 　
 『誰も守ってくれない』は、あの人気シリーズ『踊る大捜査線』チームが、“容疑者家族の保護”というテーマに挑戦したもので、『踊る大捜査線』の脚本を手掛けた君塚監督が、同作品のために警察や事件を取材してきた中から生まれた企画だそうです。第32回モントリオール世界映画祭では「最優秀脚本賞」を受賞。豪華メンバーで贈る社会派エンターテイメント作品となっています。<br>

<?php p('p4.jpg', 'c', 'みなさんからユニークなコメントも飛び出し、ようやく笑顔に'); ?>
